            <h1>Following the Money</h1>
            <p><a href="http://government.arts.cornell.edu%2Ffaculty%2Fenns">Peter Enns</a>, associate professor of government, focuses on public opinion, representation and quantitative research methods. "I identify as a political scientist who works with data, which increasingly means bigger and bigger data sets, he says. Computer skills broaden our understanding and bring in connections that wouldn’t have been available before, he adds.</p>

            <p>He’s working on a joint project with scholars at the University of Tennessee and the University of South Carolina to analyze the Congressional Record. More recent Congressional Record data is already digitized, but earlier records are in the form of PDFs, comprising almost a terabyte of data.</p> 

            <p>"In order to analyze what members of Congress are saying, we have to write code that extracts the text out of these PDFs to generate a new data set, explains Enns. With help from graduate students in the field of computer science, the researchers will be able to link this new data with existing data, such as the demographic characteristics of members of Congress and their campaign donations. Then, researchers can study topics such as connections between campaign donation and what politicians choose to speak about.</p>

            <p>Eventually, the new data set will be put in the public domain for other researchers to access.</p>          
