            <h1>Paying It Forward</h1>
            <p><a href="http://www.soc.cornell.edu/faculty/macy.html">Michael Macy</a>, Goldwin Smith Professor of Arts and Sciences in the Departments of Sociology and Information Science, is director of the <a href="http://sdl.soc.cornell.edu/">Social Dynamics Laboratory</a> (SDL), which studies the interplay between network topology (the arrangement of social ties) and the dynamics of social interaction, using computational models, data from on-line networks and experiments with human participants conducted in online "virtual labs."</p>

            <p>The SDL includes a large group of students from many different fields and colleges, including sociology, economics, applied math, information science, computer science and city and regional planning. Their projects are interdisciplinary and team-based, sharing in common the use of data for on-line networks and the use of computer simulation.  
            </p>
            <p>"The graduate students in the lab are extremely sophisticated technically and highly skilled, and it is hard to tell which students are from sociology and which are from information science , says Macy.
            </p>
            <p>SDL researchers recently published a paper on "<a href="http://www.jstor.org/stable/10.1086/681254?seq=1&#page_scan_tab_contents">Why Liberals Drink Lattes</a>" that examines how our political views and lifestyle preferences are affected by the people with whom we interact, people with whom we are thrown together and who tend to be like us. Thus, says Macy, it is critical to study human behavior and social interaction from a relational perspective, which takes into account social influence.
            </p>
            <p>"You ignore social influences and contagion at your peril, says Macy, who notes that in analyzing 40 years of classical general social surveys they found that lifestyle preferences almost all correlated with one or more demographic variables. "But what if these correlations are largely accidents of self-reinforcing dynamics of self-selection and social influence? he asks. 
            </p>
            <p>Other SDL projects address the dynamics of "<a href="http://www.nytimes.com/2014/03/16/opinion/sunday/the-science-of-paying-it-forward.html?_r=0">pay it forward</a>" in which people help a stranger, and whether it is possible to predict whether something on Twitter will go viral. 
            </p>
            <p>Another project, timely for this election season, is one that examines followers of the U.S. Congress to study political and cultural polarization. "Five million people follow members of Congress and they’re co-following other things, explains Macy. "So if you find correlations between lifestyle preferences (such as American football vs. soccer, or bands people follow), we can look to see if political preferences and other cultural preferences also divide politically. </p>
