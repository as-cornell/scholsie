            <h1>Digging into Data</h1>
            <p><a href="http://linguistics.cornell.edu/mats-rooth">Mats Rooth</a>, professor of linguistics and information Science and director of the Computational Linguistics Lab, does research in computational linguistics and natural language semantics. In 2010, <a href="http://www.news.cornell.edu/stories/2010/01/linguist-uses-internet-study-how-we-say-things">he was one of the winners</a> of an international competition, Digging into Data, that challenged scholars to devise innovative humanities and social science research projects using large-scale data analysis. His project, Harvesting Speech Datasets for Linguistic Research on the Web, looked at distinctions of prosody (rhythm, stress and intonation) in spoken language. He used software to search for word patterns in text transcriptions of audio and video files.</p>
            <p>Using the Internet to harvest hundreds or thousands of examples of spontaneous, rather than lab-created, use of word patterns enables researchers to evaluate theories about the form and meaning of prosody on an unprecedented scale; such research has had a transformative effect on the understanding of prosody, says Rooth.</p>
