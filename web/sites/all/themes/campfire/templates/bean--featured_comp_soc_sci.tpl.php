      <h1 class="sectionTitle">Pioneers across campus</h1>
      <div class="as-columns--two">
        <p>
          Cornell's Information Science (IS) Department, one of the first of its kind, celebrated its tenth anniversary last year. IS was founded as an interdisciplinary effort between computer science and social science and unites faculty from the College of Arts & Sciences, the Faculty of Computer Science and the College of Agriculture and Life Sciences, to examine information systems in their social, cultural, economic, historical, legal, and political contexts. This department forms the heart of collaborative efforts between social and computational scientists at Cornell.
        </p>
        <p>
          "An exciting thing that happened at Cornell is that we managed to find each other across a range of disciplines and start working together, which unlocked the latent potential energy in this area," says Kleinberg.
        </p>
        <p>Kleinberg was one of Cornell's early computational science pioneers. While a student at Cornell he studied with leaders in the field, Daniel Huttenlocher, founding Dean and Vice Provost of Cornell Tech, and Éva Tardos, Jacob Gould Schurman Professor of Computer Science; he now works with them on collaborative projects. Other early pioneers at Cornell include Duncan Watts , Ph.D. '97 and Steven Strogatz, Jacob Gould Schurman Professor of Applied Mathematics, who researched networks together; David Easley, Henry Scarborough Professor of Social Science and professor of information science, and Lawrence Blume, Goldwin Smith Professor of Economics, who used computational ideas in their economics research; Macy, an early adopter of computational techniques in social sciences; and Geri Gay, Chair and Kenneth J. Bissett Professor of Communication, who uses insights from social science research as the basis for software interface design.</p>
        <p>
          Many of the early participants in the Information Science Department were involved in a three-year project sponsored by Cornell's Institute for Social Sciences (ISS) in 2005, "<a href="http://socialsciences.cornell.edu/networks/">Getting Connected: Social Science in the Age of Networks</a>." The project sought to tap the expertise, tools and skills of network analysts across the university, from computer scientists archiving the Web to social psychologists studying adolescent behavior. The project's aim, to build a culture for collaborative research across disciplines and colleges, succeeded beyond what anyone might have expected, says Kleinberg, and has had a lasting impact on social science at Cornell.
        </p>
        <p><img src="/sites/as/files/networks-2.jpg" alt="Networks"></p>
        <p>
          "ISS gave us this space to spend time thinking how to develop and define an academic discipline at the increasingly complex boundary where human audiences intersect with computing technology," says Kleinberg.  "The ISS project let us stretch and think about things you couldn't create if not for this special time."
        </p>
        <p>
          Many of the professors involved in that network project have continued as leaders in the field of computational social sciences. Among them is Macy, who was the project leader for the ISS network project and is currently director of the <a href="http://sdl.soc.cornell.edu/">Social Dynamics Laboratory</a>.  His work uses big data such as the telephone logs of nearly every call in the UK and hundreds of millions of Twitter users. "We are really only at the beginning of exploring what these big data can do for researchers," says Macy.
        </p>
