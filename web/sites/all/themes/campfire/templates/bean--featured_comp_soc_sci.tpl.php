        <h1 class="sectionTitle">Cornell at forefront of computational social science</h1>

        <div class="as-grid as-grid--two-one">
          <div>
            <p>Cornell University is uniquely suited because of its interdisciplinary character to grasp the opportunities afforded by these new challenges in social sciences and has been at the forefront of developing the new field of computational social science.</p>

            <p>"The interdisciplinary cooperation necessary to successfully conduct research in this field is a hallmark of Cornell and is a major reason for Cornell's leadership in the field," said <a href="http://as.cornell.edu/college-leadership">Gretchen Ritter</a>, the Harold Tanner Dean of Arts &amp; Sciences.</p>

            <p>Cornell's collaborative nature gives information and computer scientists and applied mathematicians the theoretical landscape to discover interesting and important questions to which their tools can be put to work.</p>

            <p>While a great deal of collaboration is ongoing with economics and sociology, computer scientists are looking forward to more opportunities to interact with researchers in the fields of anthropology, psychology, law and business, says <a href="http://infosci.cornell.edu/faculty/jon-kleinberg" title="Jon Kleinberg">Jon Kleinberg</a> '93, chair and professor of information science and professor of computer science.</p>

            <p><a href="http://infosci.cornell.edu/faculty/david-mimno">David Mimno</a>, assistant professor of information science, embodies the interdisciplinary nature of the field. He works at the intersection of computing and the humanities, using technology to advance scholarship. He teaches a course called <a href="http://mimno.infosci.cornell.edu/info3350">Text Mining for History and Literature</a>, and he's exploring ways to take advantage of large non-numerical data sets like haiku, novels, and runaway slave ads using algorithmic approaches to data.</p>

            <p>Mimno offers a novel definition of digital humanities scholars: "We are basically ethnographers," he says, "studying humanists and computer scientists and seeing how they can relate and use each other's tools."</p>
