        <p>How can we avoid another cascading failure of the stock market? Why has the United States become so politically polarized? Where is Ebola likely to strike next? Why do some terrorist organizations use suicide bombers, while others do not?</p>
      </div>
      <div class="as-columns--two">
        <p>
          <strong>Answers may well be found through the new field of computational social science</strong>.
        </p>
        <p>
          Growing use of the Internet and social media in the past decade has led to an explosion in the amount of social and behavioral data available to researchers. This in turn has created huge opportunities for social scientists to study human behavior and social interaction in unprecedented detail. Leveraging these opportunities requires collaborative, interdisciplinary efforts involving computer and information scientists, physicists, and mathematicians who know how to build the "telescope" and economists, political scientists, and sociologists who know where to aim it.
        </p>
        <p>
          Computational social science exists at the intersection of these varied disciplines, offering a wide range of tools and research methodologies that were previously not available to social and behavioral scientists.
          "The kind of analysis we're doing in computational social science used to require punch cards and giant machines which couldn't do a fraction of what we can do now," notes Peter Enns, associate professor of government.
        </p>
        <p>
          Robert Braun, a doctoral student in the field of government, has harnessed these new technologies to investigate suicide bombers and resistance to genocide, among other topics.
        </p>
        <p>
          "For the past 50 years the social sciences have relied heavily on surveys for data about human behavior, surveys typically done with at most a few thousand participants, none of whom know each other," says <a href="http://infosci.cornell.edu/faculty/michael-macy">Michael Macy</a>, Goldwin Smith Professor of Arts and Sciences in Sociology and Information Science, Director of the Social Dynamics Laboratory. "These independent observations are not very useful for studying social influences on behavior. Now we have global data from social media like Twitter and other social media platforms, that allow us for the first time to study social influences on human behavior and interpersonal interaction at population scale. And that's pretty exciting."
        </p>
