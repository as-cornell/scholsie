        <h1 class="sectionTitle">Research Collaborations Continue</h1>     
        <p>
          In September 2015, Cornell hosted a conference in Clark Hall showcasing cutting-edge research in the field of computational social science, featuring alumni and other noted scholars. The conference coincided with the opening of a joint search for a position in computational social science by the College of Arts &amp; Sciences and the Faculty of Computing and Information Science.
        </p>
        <p>
          <a href="http://www.cs.cornell.edu/~jgm/">Greg Morrisett</a>, dean of the Faculty of Computing and Information Science, said computational social science has far-reaching applications in the fields of security, privacy, sociology and government.
        </p>
        <p>
          Watts, now a principal researcher and founding member of Microsoft Research Lab in Manhattan and a Cornell A.D. White Professor-at-Large, noted that social phenomena, which arise when individuals interact to produce collective behavior, are hard to study empirically.
        </p>
        <p>
          "It's hard to do science when you can't measure what you care about and you can't do experiments," said Watts. "The web is lifting these historic barriers. There's a dramatic increase in scale, scope and granularity of data. We can examine data at the individual level with emails, e-commerce, social media, etc." Web platforms, he said, are another revolution, increasing the speed and scale of experiments.
        </p>
        <p>
          "This conference is really a celebration of a transformative moment in social science, not just because of technology but by the vision of what that technology can do," said Watts. "CSS research is motivated by old questions that bring new capabilities to bear."
        </p>
        <blockquote class="full gold">
          <p>"This conference is really a celebration of a transformative moment in social science, not just because of technology but by the vision of what that technology can do. CSS research is motivated by old questions that bring new capabilities to bear."</p>
          <p>Duncan Watts, Ph.D. '97</p>
        </blockquote>
        <p>
          <a href="http://web.media.mit.edu/~sandy/">Sandy Pentland</a>, professor at the Massachusetts Institute of Technology and recently named by Forbes as one of the seven most important computer scientists in the world, told conference attendees that enormous data sets and computerized monitoring allow a level of accuracy unattainable by traditional survey methods.
        </p>
        <p>
          Pentland also pointed out that social data can be used to measure U.N. Millennial Goals such as fixed migration, genocide, propagation of infectious diseases can all be measured. "Change happens when things are measured – you can mobilize resources more quickly and rigorously."
        </p>
        <p>
          Other speakers included alumni <a href="http://scholar.harvard.edu/sendhil/home">Sendhil Mullainathan</a> '93, professor of economics at Harvard University, and Lars Backstrom '04, MS '08, Ph.D. ‘09, head of News Feed for Facebook, and industry representatives Lada Adamic, head of Facebook's Product Science Group and David Pennock, Microsoft Research's principal research and assistant managing director.
        </p>
        <p>As conference participants highlighted, the tools offered by computational social science are making important contributions to critical social, economic, and political issues today. Cornell’s researchers actively engage with real-world problems, reaching across the disciplines in multi-college collaborations.</p>
